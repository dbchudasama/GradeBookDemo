using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Grades
{
    class Program
    {
        static void Main(string[] args)
        {
            //Creating a new instance of the class
            Grades book = new Grades();

            //Adding score 91
            book.AddGrade(91);
            //Adding score 85.9
            book.AddGrade(85.9f);
            //Adding score 75
            book.AddGrade(75);

            //Storing the ComputeStatistics reference in the stats variable.
            GradeStatistics stats = GradeState.Calculate(book.GetGrades());

            Console.WriteLine("Highest Grade is:" + stats.HighestGrade);
            Console.WriteLine("Lowest Grade is:" + stats.LowestGrade);
            Console.WriteLine("Average Grade is:" + stats.AverageGrade);

            //Using readline to allow the console to stay open and display above message until a keyboard value is pressed.
            Console.ReadLine();

        }
    }
}
